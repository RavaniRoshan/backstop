def test_runner_applies_mocked_llm_output_to_worktree(tmp_path, monkeypatch):
    """End-to-end via the LLM path: mocked model output is written to the
    worktree, test_command runs, and test_passed reflects the exit code."""
    import pathlib

    # A real (non-git) repo dir whose main.py matches the patch target line.
    (tmp_path / "main.py").write_text("# line1\n# line2\n# Generated by t1\n")

    runner = WedgeRunner(
        "llm1",
        repo_path=str(tmp_path),
        provider="openai",
        base_url="https://my-proxy.example.com/v1",
    )
    # Recorded/mock LLM response.
    async def mock_llm(prompt):
        return FAKE_PATCH

    monkeypatch.setattr(runner, "_generate_patch_from_llm", mock_llm)
    # Keep the worktree so we can inspect it after the run.
    monkeypatch.setattr(runner, "_teardown_worktree", lambda: None)

    async def go():
        return await runner.run("refactor main.py", "true")

    r = asyncio.run(go())

    assert r["patch_applied"] is True
    wt = pathlib.Path(runner.worktree_path)
    assert "# patched hello world" in (wt / "main.py").read_text()
    # `true` exits 0
    assert r["test_passed"] is True


def test_cli_passes_base_url_from_task_yaml(monkeypatch, tmp_path):
    """`wedge run` reads base_url from task.yaml (and WEDGE_BASE_URL) and
    forwards it to each WedgeRunner."""
    captured = {}

    class FakeRunner:
        def __init__(self, runner_id, repo_path, provider="anthropic", base_url=None, model=None):
            captured[runner_id] = base_url

        async def run(self, prompt, test_cmd, patch=None):
            return {
                "runner_id": "x",
                "patch": {},
                "patch_applied": True,
                "test_passed": True,
                "budget_remaining": None,
            }

    monkeypatch.setattr(wedge_cli, "WedgeRunner", FakeRunner)
    monkeypatch.setattr(wedge_cli, "compare_patches", lambda patches: {})
    monkeypatch.setattr(wedge_cli, "generate_report", lambda *a, **k: "")

    task_file = tmp_path / "task.yaml"
    task_file.write_text(
        yaml.safe_dump(
            {
                "name": "t",
                "prompt": "p",
                "test_command": "true",
                "runners": 2,
                "provider": "openai",
                "base_url": "https://custom.example.com/v1",
            }
        )
    )

    async def go():
        await wedge_cli.run_task(str(task_file))

    asyncio.run(go())
    assert captured == {"R0": "https://custom.example.com/v1",
                         "R1": "https://custom.example.com/v1"}


