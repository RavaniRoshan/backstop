"""Offline test for WedgeRunner patch application and test_command execution.

This drives WedgeRunner against an offline patch (no live LLM call) and
verifies the patch is written to a worktree, test_command is executed, and
test_passed reflects the real exit code. Also covers default models,
base_url override, and provider dispatch.
"""
import asyncio
import os
import subprocess

import pytest
import yaml

import wedge.cli as wedge_cli
from wedge.runner import WedgeRunner, _extract_patch_from_output, apply_patch


FAKE_PATCH = (
    "--- a/main.py\n"
    "+++ b/main.py\n"
    "@@ -3 +3 @@\n"
    "-# Generated by t1\n"
    "+# patched hello world\n"
)


def test_extract_clean_diff():
    out = _extract_patch_from_output(
        "loose text\n--- a/x.py\n+++ b/x.py\n@@\n-old\n+new\n"
    )
    assert out.lstrip().startswith("--- a/x.py")


def test_extract_search_replace_block():
    out = _extract_patch_from_output("### search: foo\n### replace: foo2")
    assert "foo" in out and "foo2" in out


def test_extract_returns_empty_for_plain_text():
    assert _extract_patch_from_output("no diff here") == ""


def test_apply_patch_modifies_real_file(tmp_path):
    f = tmp_path / "hello.txt"
    f.write_text("line1\nold\nline3\n")
    patch = (
        "--- a/hello.txt\n"
        "+++ b/hello.txt\n"
        "@@ -1,3 +1,3 @@\n"
        " line1\n"
        "-old\n"
        "+new\n"
        " line3\n"
    )
    assert apply_patch(patch, str(f))
    assert "new" in f.read_text()
    assert "old" not in f.read_text()


def test_runner_writes_patch_and_executes_test_command(monkeypatch):
    """End-to-end: patch is written to worktree, test_command runs, exit
    code is honored."""
    # Force a clean, non-repo worktree path (deterministic)
    tmp = monkeypatch.setattr("wedge.runner.os.path.exists", lambda p: False)
    # Use temp repo path so runner falls back to mkdtemp
    runner = WedgeRunner(
        "t1",
        repo_path="/tmp/nonexistent_for_runner",
        provider="openai",
        base_url="https://my-proxy.example.com/v1",
    )
    assert runner.base_url == "https://my-proxy.example.com/v1"

    # `false` exits 1
    async def go():
        return await runner.run("ignored", "false", patch=FAKE_PATCH)

    r = asyncio.run(go())
    assert r["runner_id"] == "t1"
    assert r["patch"]["main.py"] == FAKE_PATCH
    # exit code 1 -> test_passed False
    assert r["test_passed"] is False

    # `true` exits 0
    async def go2():
        return await runner.run("ignored", "true", patch=FAKE_PATCH)

    r2 = asyncio.run(go2())
    assert r2["test_passed"] is True


